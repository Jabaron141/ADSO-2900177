const pornot = function(pnot, pporc) {
    let not = pnot;
    let porce = pporc;
    let rnot;
    rnot = not * porce;
    return rnot;
}

const suma = function(pnumUno, pnumDos, pnumTre) {
    let sumar;
    numUno = pnumUno;
    numDos = pnumDos;
    numTre = pnumTre;
    sumar = numUno + numDos + numTre;
    return sumar
}