function pornot(pnot, pporc) {
    let not = pnot;
    let porce = pporc;
    let rnot;
    rnot = not * porce;
    return rnot;
}

function suma(pnumUno, pnumDos, pnumTre) {
    let sumar;
    numUno = pnumUno;
    numDos = pnumDos;
    numTre = pnumTre;
    sumar = numUno + numDos + numTre;
    return sumar
}