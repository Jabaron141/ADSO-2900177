const pornot = function(pnot, pporc) {
    let not = pnot;
    let porce = pporc;
    let rnot;
    rnot = not * porce;
    return rnot;
}

const suma = function(pnumUno, pnumDos, pnumTre) {
    let numUno = pnumUno;
    let numDos = pnumDos;
    let numTre = pnumTre;
    let sumar;
    sumar = numUno + numDos + numTre;
    return sumar;
}